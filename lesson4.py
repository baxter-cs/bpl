# Lesson 4
# Operators

a = 5

a += 2   # Equiv. to a = a + 2

print(a) # 7

a -= 2   # Equiv. to a = a - 2

print(a) # 5

a *= 2   # Equiv. to a = a * 2

print(a) # 10

a /= 2   # Equiv. to a = a / 2

print(a) # 5

a **= 2  # Equiv. to a = a**2

print(a) # 25

a %= 2   # Equiv. to a = a % 2

print(a) # 1
